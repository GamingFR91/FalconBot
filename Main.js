"use strict"
import core from './FalconBase'

class Main{

    launch(){

        core.registerGuilds();

        core.launchBot();

        core.launchMusic();

        core.launchHandlers();

        core.exitHandler();
    }
}
    // Launch the bot
    const main = new Main;
    main.launch();

module.exports = new Main


